function scoreDisease(entry, selectedSymptoms) {
  return entry.signs.filter((item) => selectedSymptoms.includes(item)).length;
}

export function getDiseaseResult(crop, selectedSymptoms) {
  const options = diseaseLibrary[crop] ?? [];
  const ranked = [...options].sort(
    (left, right) => scoreDisease(right, selectedSymptoms) - scoreDisease(left, selectedSymptoms),
  );

  return ranked[0] ?? {
    name: 'General crop stress',
    medicine: 'Use a mild micronutrient spray and observe for 2 days.',
    prevention: 'Check irrigation, nutrient balance, and recent pest movement.',
  };
}

